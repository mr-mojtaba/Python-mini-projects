# یک ژنراتور بی نهایت از اعداد زوج یا فرد بنوییسید. زوج یا فرد بودن توسط کاربر تعیین میشود.

def e_o(enter):
    c = 0
    if enter == "o":
        c = 1
    while True:
        yield c
        c += 2

s = e_o("o")

for i in range(10):
    print(next(s))
